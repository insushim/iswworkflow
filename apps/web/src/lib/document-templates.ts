export function searchTemplates(query: string): DocumentTemplate[] {
  const queryLower = query.toLowerCase();
  const queryWords = queryLower.split(/\s+/).filter(w => w.length > 0);

  return documentTemplates
    .map(template => {
      let score = 0;

      // ì œëª© ë§¤ì¹­ (ê°€ì¤‘ì¹˜ ë†’ìŒ)
      if (template.title.toLowerCase().includes(queryLower)) {
        score += 100;
      }

      // í‚¤ì›Œë“œ ë§¤ì¹­
      for (const keyword of template.keywords) {
        if (queryLower.includes(keyword.toLowerCase())) {
          score += 50;
        }
        for (const word of queryWords) {
          if (keyword.toLowerCase().includes(word)) {
            score += 20;
          }
        }
      }

      // ì¹´í…Œê³ ë¦¬ ë§¤ì¹­
      if (template.category.toLowerCase().includes(queryLower)) {
        score += 30;
      }

      // ì„œë¸Œì¹´í…Œê³ ë¦¬ ë§¤ì¹­
      if (template.subcategory.toLowerCase().includes(queryLower)) {
        score += 25;
      }

      return { template, score };
    })
    .filter(item => item.score > 0)
    .sort((a, b) => b.score - a.score)
    .map(item => item.template);
}

