// 인기 템플릿 가져오기
export const getPopularTemplates = (limit = 10): DocumentTemplate[] => {
  return [...documentTemplates]
    .sort((a, b) => b.popularity - a.popularity)
    .slice(0, limit);
};

// 카테고리별 템플릿 가져오기
export const getTemplatesByCategory = (categoryId: string): DocumentTemplate[] => {
  return documentTemplates.filter((t) => t.category === categoryId);
};

// 검색
export const searchTemplates = (query: string): DocumentTemplate[] => {
  const lowerQuery = query.toLowerCase();
  return documentTemplates.filter(
    (t) =>
      t.name.toLowerCase().includes(lowerQuery) ||
      t.description.toLowerCase().includes(lowerQuery) ||
      t.tags.some((tag) => tag.toLowerCase().includes(lowerQuery))
  );
};
