export const getSafetyEducationsByDomain = (domain: string): SafetyEducation[] => {
  return safetyEducations.filter((edu) => edu.domain === domain);
};

export const getSafetyEducationsByGrade = (grade: number): SafetyEducation[] => {
  return safetyEducations.filter((edu) => edu.grade.includes(grade));
};

export const getEmergencyProceduresByType = (type: string): EmergencyProcedure[] => {
  return emergencyProcedures.filter((proc) => proc.type === type);
};

export const getSafetyChecklistByLocation = (location: string): SafetyChecklist | undefined => {
  return safetyChecklists.find((list) => list.location === location);
};

export const searchSafetyEducations = (query: string): SafetyEducation[] => {
  const lowerQuery = query.toLowerCase();
  return safetyEducations.filter(
    (edu) =>
      edu.title.toLowerCase().includes(lowerQuery) ||
      edu.content.toLowerCase().includes(lowerQuery) ||
      edu.keywords.some((keyword) => keyword.toLowerCase().includes(lowerQuery))
  );
};
