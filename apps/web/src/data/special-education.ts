export const getDisabilityInfo = (name: string): DisabilityInfo | undefined => {
  return disabilityInfos.find((info) => info.name === name);
};

export const getTipsByCategory = (category: string): SpecialEducationTip[] => {
  return specialEducationTips.filter((tip) => tip.category === category);
};

export const getTipsByDisability = (disability: string): SpecialEducationTip[] => {
  return specialEducationTips.filter(
    (tip) => !tip.targetDisability || tip.targetDisability.includes(disability)
  );
};

export const getAccommodationsByArea = (area: string): AccommodationStrategy[] => {
  return accommodationStrategies.filter((acc) => acc.area === area);
};

export const getAccommodationsByDisability = (disability: string): AccommodationStrategy[] => {
  return accommodationStrategies.filter((acc) => acc.applicableTo.includes(disability));
};

export const searchSpecialEducation = (query: string): SpecialEducationTip[] => {
  const lowerQuery = query.toLowerCase();
  return specialEducationTips.filter(
    (tip) =>
      tip.title.toLowerCase().includes(lowerQuery) ||
      tip.content.toLowerCase().includes(lowerQuery) ||
      tip.keywords.some((keyword) => keyword.toLowerCase().includes(lowerQuery))
  );
};

