export const getTermsByCategory = (category: string): GlossaryTerm[] => {
  return glossaryTerms.filter((term) => term.category === category);
};

export const getAbbreviationsByCategory = (category: string): AbbreviationEntry[] => {
  return abbreviations.filter((abbr) => abbr.category === category);
};

export const searchTerms = (query: string): GlossaryTerm[] => {
  const lowerQuery = query.toLowerCase();
  return glossaryTerms.filter(
    (term) =>
      term.term.toLowerCase().includes(lowerQuery) ||
      term.definition.toLowerCase().includes(lowerQuery) ||
      term.abbreviation?.toLowerCase().includes(lowerQuery) ||
      term.keywords.some((keyword) => keyword.toLowerCase().includes(lowerQuery))
  );
};

export const searchAbbreviations = (query: string): AbbreviationEntry[] => {
  const lowerQuery = query.toLowerCase();
  return abbreviations.filter(
    (abbr) =>
      abbr.abbreviation.toLowerCase().includes(lowerQuery) ||
      abbr.fullName.toLowerCase().includes(lowerQuery) ||
      abbr.description.toLowerCase().includes(lowerQuery)
  );
};

export const getTermByAbbreviation = (abbr: string): GlossaryTerm | undefined => {
  return glossaryTerms.find(
    (term) => term.abbreviation?.toLowerCase() === abbr.toLowerCase()
  );
};

